#include "fillit.h"

void	remove_tmino(char **map, char id)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (map[x])
	{
		while (map[x][y])
		{
			if (map[x][y] == id)
				map[x][y] = '.';
			y++;
		}
		y = 0;
		x++;
	}
}
